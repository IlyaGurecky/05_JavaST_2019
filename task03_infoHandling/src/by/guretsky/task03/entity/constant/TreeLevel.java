package by.guretsky.task03.entity.constant;

/**
 * Enum class for select text component level.
 *
 * @author ilyaguretsky
 */
public enum TreeLevel {
    /**
     * Text level.
     */
    TEXT,
    /**
     * Paragraph level.
     */
    PARAGRAPH,
    /**
     * Sentence level.
     */
    SENTENCE,
    /**
     * Lexeme level.
     */
    LEXEME,
    /**
     * Word level.
     */
    WORD,
    /**
     * Expression level.
     */
    EXPRESSION,
    /**
     * Punctuation mark level.
     */
    PUNCTUATION,
    /**
     * Symbol level.
     */
    SYMBOLS
}
